//! Self-contained HTML report — Rust port of the Java `HtmlReport`: inline
//! CSS + inline SVG only (log-scaled latency histogram + percentile plot per
//! workload), same layout and styling so a Rust record sits side-by-side with
//! the saved Java records for comparison.

use std::fmt::Write as _;

use crate::stats::{Stats, EDGE_LABELS};
use crate::workload::WorkloadResult;

const COLORS: [&str; 4] = ["#2563eb", "#db2777", "#059669", "#d97706"];
const W: f64 = 760.0;
const HT: f64 = 300.0;

pub fn render(env: &[(String, String)], results: &[WorkloadResult]) -> String {
    let mut h = String::with_capacity(1 << 16);
    h.push_str("<!DOCTYPE html>\n<html lang=\"en\">\n<head>\n<meta charset=\"utf-8\">\n");
    h.push_str("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">\n");
    h.push_str("<title>Mercury benchmark report (Rust)</title>\n<style>\n");
    h.push_str(CSS);
    h.push_str("\n</style>\n</head>\n<body>\n");
    h.push_str("<h1>Mercury benchmark report (Rust)</h1>\n");
    h.push_str(
        "<p class=\"sub\">End-to-end latency &amp; throughput in a single process. \
         <b>Normal operation</b> scenarios keep the arrival rate at or below consumer capacity, so the \
         ElasticQueue stays within its in-memory buffer; the <b>overload</b> scenario drives it into \
         the disk-backed back-pressure buffer. Together they show how the event system behaves both in \
         healthy steady state and under sustained overload.</p>\n",
    );
    // environment
    h.push_str("<div class=\"card\"><h2>Environment</h2>\n<table class=\"env\">\n");
    for (key, value) in env {
        let _ = writeln!(
            h,
            "<tr><th>{}</th><td>{}</td></tr>",
            escape(key),
            escape(value)
        );
    }
    h.push_str("</table></div>\n");
    // workloads, grouped by category
    let mut last_category = "";
    for (i, r) in results.iter().enumerate() {
        if r.category != last_category {
            let _ = writeln!(h, "<h2 class=\"category\">{}</h2>", escape(&r.category));
            last_category = &r.category;
        }
        let color = COLORS[i % COLORS.len()];
        let s = &r.stats;
        h.push_str("<div class=\"card\">\n");
        let _ = writeln!(
            h,
            "<h2><span class=\"dot\" style=\"background:{color}\"></span>{}</h2>",
            escape(&r.name)
        );
        let _ = writeln!(h, "<p class=\"desc\">{}</p>", escape(&r.description));
        // summary chips
        h.push_str("<div class=\"chips\">\n");
        chip(
            &mut h,
            "throughput",
            &format!("{} ops/s", num0(r.throughput())),
        );
        chip(&mut h, "ok", &group(s.count));
        chip(&mut h, "failures", &group(r.failures));
        chip(&mut h, "elapsed", &format!("{:.2} s", r.elapsed_sec));
        for (key, value) in &r.params {
            chip(&mut h, key, value);
        }
        h.push_str("</div>\n");
        // stats table
        h.push_str(
            "<table class=\"stats\">\n<thead><tr><th>min</th><th>mean</th><th>std dev</th>\
             <th>p50</th><th>p90</th><th>p99</th><th>p99.9</th><th>p99.99</th><th>max</th>\
             </tr></thead>\n<tbody><tr>",
        );
        for v in [
            s.min_ms,
            s.mean_ms,
            s.stddev_ms,
            s.p50,
            s.p90,
            s.p99,
            s.p999,
            s.p9999,
            s.max_ms,
        ] {
            let _ = write!(h, "<td>{}</td>", ms(v));
        }
        h.push_str("</tr></tbody></table>\n<p class=\"unit\">latencies in milliseconds</p>\n");
        // charts
        h.push_str("<div class=\"charts\">\n");
        h.push_str("<div class=\"chart\"><h3>Latency distribution</h3>\n");
        h.push_str(&svg_histogram(s, color));
        h.push_str("</div>\n");
        h.push_str("<div class=\"chart\"><h3>Latency by percentile</h3>\n");
        h.push_str(&svg_percentiles(s, color));
        h.push_str("</div>\n</div>\n</div>\n");
    }
    h.push_str(
        "<p class=\"foot\">Generated by benchmark-reporter (Rust) — a self-contained Mercury \
         performance harness. Re-run with different <code>-Dbench.*</code> settings to compare; \
         Java reference records live in the mercury-composable repository.</p>\n",
    );
    h.push_str("</body>\n</html>\n");
    h
}

fn svg_open() -> String {
    format!(
        "<svg viewBox=\"0 0 {W:.0} {HT:.0}\" xmlns=\"http://www.w3.org/2000/svg\" class=\"svg\">\n"
    )
}

// ---- SVG: log-scaled histogram of latency buckets ----
fn svg_histogram(s: &Stats, color: &str) -> String {
    let (left, right, top, bottom) = (44.0, 12.0, 14.0, 60.0);
    let plot_w = W - left - right;
    let plot_h = HT - top - bottom;
    let bins = &s.bin_counts;
    let max = bins.iter().copied().max().unwrap_or(0).max(1);
    let log_max = ((max as f64) + 1.0).log10();
    let slot = plot_w / bins.len() as f64;
    let bar_w = slot * 0.78;
    let mut g = svg_open();
    g.push_str(&line(
        left,
        top + plot_h,
        left + plot_w,
        top + plot_h,
        "#d1d5db",
    ));
    for (i, &count) in bins.iter().enumerate() {
        let x = left + i as f64 * slot + (slot - bar_w) / 2.0;
        if count > 0 {
            let bh = plot_h * (((count as f64) + 1.0).log10() / log_max);
            let y = top + plot_h - bh;
            let _ = writeln!(
                g,
                "<rect x=\"{x:.2}\" y=\"{y:.2}\" width=\"{bar_w:.2}\" height=\"{bh:.2}\" \
                 fill=\"{color}\" rx=\"1\"><title>{}: {}</title></rect>",
                EDGE_LABELS[i],
                group(count)
            );
        }
        let lx = left + i as f64 * slot + slot / 2.0;
        let ly = top + plot_h + 10.0;
        let _ = writeln!(
            g,
            "<text x=\"{lx:.2}\" y=\"{ly:.2}\" class=\"tick\" text-anchor=\"end\" \
             transform=\"rotate(-45 {lx:.2} {ly:.2})\">{}</text>",
            EDGE_LABELS[i]
        );
    }
    let _ = writeln!(
        g,
        "<text x=\"{left:.2}\" y=\"{:.2}\" class=\"axis\">count (log scale) — peak {}</text>",
        top - 3.0,
        group(max)
    );
    g.push_str("</svg>");
    g
}

// ---- SVG: latency-by-percentile "nines" plot ----
fn svg_percentiles(s: &Stats, color: &str) -> String {
    let (left, right, top, bottom) = (56.0, 16.0, 16.0, 44.0);
    let plot_w = W - left - right;
    let plot_h = HT - top - bottom;
    let labels = ["50%", "90%", "99%", "99.9%", "99.99%", "max"];
    let vals = [s.p50, s.p90, s.p99, s.p999, s.p9999, s.max_ms];
    let mut y_max = vals.iter().cloned().fold(0.0_f64, f64::max);
    if y_max <= 0.0 {
        y_max = 1.0;
    }
    y_max *= 1.1;
    let mut g = svg_open();
    for t in 0..=4 {
        let v = y_max * t as f64 / 4.0;
        let y = top + plot_h - plot_h * (t as f64 / 4.0);
        g.push_str(&line(left, y, left + plot_w, y, "#eef1f5"));
        let _ = writeln!(
            g,
            "<text x=\"{:.2}\" y=\"{:.2}\" class=\"tick\" text-anchor=\"end\">{}</text>",
            left - 6.0,
            y + 3.0,
            ms(v)
        );
    }
    let step = plot_w / (labels.len() - 1) as f64;
    let mut pts = String::new();
    for (i, &v) in vals.iter().enumerate() {
        let x = left + i as f64 * step;
        let y = top + plot_h - plot_h * (v / y_max);
        let _ = write!(pts, "{x:.2},{y:.2} ");
    }
    let _ = writeln!(
        g,
        "<polyline points=\"{}\" fill=\"none\" stroke=\"{color}\" stroke-width=\"2\"/>",
        pts.trim()
    );
    for (i, &v) in vals.iter().enumerate() {
        let x = left + i as f64 * step;
        let y = top + plot_h - plot_h * (v / y_max);
        let _ = writeln!(
            g,
            "<circle cx=\"{x:.2}\" cy=\"{y:.2}\" r=\"3.5\" fill=\"{color}\"/>"
        );
        let _ = writeln!(
            g,
            "<text x=\"{x:.2}\" y=\"{:.2}\" class=\"pt\" text-anchor=\"middle\">{}</text>",
            y - 8.0,
            ms(v)
        );
        let _ = writeln!(
            g,
            "<text x=\"{x:.2}\" y=\"{:.2}\" class=\"tick\" text-anchor=\"middle\">{}</text>",
            top + plot_h + 16.0,
            labels[i]
        );
    }
    let _ = writeln!(
        g,
        "<text x=\"{:.2}\" y=\"{:.2}\" class=\"axis\">latency ms</text>",
        left - 48.0,
        top + plot_h / 2.0
    );
    g.push_str("</svg>");
    g
}

fn line(x1: f64, y1: f64, x2: f64, y2: f64, stroke: &str) -> String {
    format!(
        "<line x1=\"{x1:.2}\" y1=\"{y1:.2}\" x2=\"{x2:.2}\" y2=\"{y2:.2}\" \
         stroke=\"{stroke}\" stroke-width=\"1.0\"/>\n"
    )
}

fn chip(h: &mut String, k: &str, v: &str) {
    let _ = writeln!(
        h,
        "<span class=\"chip\"><b>{}</b> {}</span>",
        escape(k),
        escape(v)
    );
}

/// Latency formatting: 3 decimals under 1 ms, else 2 (Java parity).
fn ms(v: f64) -> String {
    if v < 1.0 {
        format!("{v:.3}")
    } else {
        group_f(v, 2)
    }
}

fn num0(v: f64) -> String {
    group_f(v, 0)
}

/// Thousands-grouped float (US locale, Java parity).
fn group_f(v: f64, decimals: usize) -> String {
    let text = format!("{v:.decimals$}");
    let (int_part, frac) = match text.split_once('.') {
        Some((i, f)) => (i.to_string(), Some(f.to_string())),
        None => (text, None),
    };
    let grouped = group_int(&int_part);
    match frac {
        Some(f) => format!("{grouped}.{f}"),
        None => grouped,
    }
}

fn group(v: u64) -> String {
    group_int(&v.to_string())
}

fn group_int(digits: &str) -> String {
    let (sign, digits) = match digits.strip_prefix('-') {
        Some(rest) => ("-", rest),
        None => ("", digits),
    };
    let mut out = String::new();
    for (i, c) in digits.chars().enumerate() {
        if i > 0 && (digits.len() - i) % 3 == 0 {
            out.push(',');
        }
        out.push(c);
    }
    format!("{sign}{out}")
}

fn escape(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
}

